  mounted() {
    axios.get('http://localhost:3001/api/jobs')
      .then((res) => {
        console.log(res.data)
        this.jobs = res.data;
        this.searchJobs();
      })
      .catch((err) => {
        console.error('‚ùå ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏á‡∏≤‡∏ô‡∏•‡πâ‡∏°‡πÄ‡∏´‡∏•‡∏ß:', err);
